            i = dataTab;

            $('.dotCircle').css({
                "transform": "rotate(" + (360 - (i - 1) * 36) + "deg)",
                "transition": "2s"
            });
            $('.itemDot').css({
                "transform": "rotate(" + ((i - 1) * 36) + "deg)",
